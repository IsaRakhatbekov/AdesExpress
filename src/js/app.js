// Script

window.onscroll = function() {
  const scrolled = window.pageYOffset || document.documentElement.scrollTop;
  const header = document.querySelector('.header')
if (header) {
  if (scrolled > 1) {
      header.classList.add('active');
  } else if (scrolled < 1) {
      header.classList.remove('active');
  }
}
};

